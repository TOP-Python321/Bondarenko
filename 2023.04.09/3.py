time = int(input("Введите время "))

# ИСПРАВИТЬ: использование синтаксиса f-строк для вывода и выражения, и его значения {=} в данном случае является лишним, так как выводит символы которых не должно быть согласно требуемому формату
print(f' {time} мин - это {time // 60 = } час {time % 60 = } мин ')


#  150 мин - это time // 60 = 2 час time % 60 = 30 мин


